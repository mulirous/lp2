package lista001;

public class Retangulo {
    public static void main(String[] args) {
        double base = 7.5;
        double altura = 12.3;
        double area = base * altura;
        double perimetro = 2 * (base + altura);

        System.out.println("Area do Retangurlo: " + area + "cm^2");
        System.out.println("Perimetro do Retangulo: " + perimetro + "cm");
    }
}