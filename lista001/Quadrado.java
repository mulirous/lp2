package lista001;

public class Quadrado {
    public static void main(String[] args) {
        double lado = 8.0;
        double area = lado * lado;
        double perimetro = 4 * lado;
        
        System.out.println("Area do Quadrado: " + area + "cm²");
        System.out.println("Perímetro do Quadrado: " + perimetro + " cm");
    }
}
